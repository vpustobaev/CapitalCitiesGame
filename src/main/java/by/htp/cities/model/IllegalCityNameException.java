package by.htp.cities.model;

public class IllegalCityNameException extends Exception {

	public IllegalCityNameException() {
		super();

	}

	public IllegalCityNameException(String message, Throwable cause) {
		super(message, cause);

	}

	public IllegalCityNameException(String message) {
		super(message);

	}

	public IllegalCityNameException(Throwable cause) {
		super(cause);

	}

}
